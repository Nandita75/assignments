// difference between pre and post increment
public class Number4 {
    public static void main(String[] args) {
        int x = 1;
        System.out.println("Pre increment results");
        System.out.println(x);
        System.out.println(++x);
        System.out.println(x);
        
        x = 1;
        System.out.println("Post increment results");
        System.out.println(x);
        System.out.println(x++);
        System.out.println(x);
    }
}

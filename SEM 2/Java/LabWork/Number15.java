public class Number15 {
    public static void main(String[] args) {
        char color = 'b';

        switch (color) {
            case 'r':
                System.out.println("red");
                break;
            case 'g':
                System.out.println("green");
                break;
            case 'b':
                System.out.println("blue");
                break;
            case 'w':
                System.out.println("white");
                break;
            default:
                System.out.println("No color");
                break;
        }
    }
}

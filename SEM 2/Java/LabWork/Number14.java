public class Number14 {
    public static void main(String[] args) {
        char color = 'b';

        switch (color) {
            case 'r':
                System.out.println("red");
            case 'g':
                System.out.println("green");
            case 'b':
                System.out.println("blue");
            case 'w':
                System.out.println("white");
            default:
                System.out.println("No color");
        }
    }
}
